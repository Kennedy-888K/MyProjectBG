# Demonstração de uso de operadores aritmétricos...
X = int(input("Digite o valor de X: "))
Y = int(input("Digite o valor de Y: "))

SOMAR = X + Y
print("O resultado da soma: " , SOMAR)

SUBTRAIR = X - Y 
print("O resultado da subtrair" , SUBTRAIR)

MULTIPLICAR = X * Y
print("O resultado da multiplicaçõ: ", MULTIPLICAR)

dividir = X / Y
print("O resultado da divisão: ", dividir)

